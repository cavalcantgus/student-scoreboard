class Student {
    constructor(id, studentName, disciplina, grade1, grade2, grade3, grade4, average, status){
        this.id = id
        this.studentName = studentName
        this.disciplina = disciplina
        this.grade1 = grade1
        this.grade2 = grade2
        this.grade3 = grade3
        this.grade4 = grade4
        this.average = average
        this.status = status
    }
}

export default Student